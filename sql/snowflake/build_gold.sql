CREATE OR REPLACE VIEW GOLD.VW_LATEST_SECURITY_SIGNALS AS
SELECT *
FROM GOLD.MART_SECURITY_TREND_SIGNALS
QUALIFY ROW_NUMBER() OVER (
    PARTITION BY SECURITY_KEY
    ORDER BY TRADING_DATE DESC
) = 1;
